
public class javabasics{
    public static void main(String args[]) {
        // if-else statement basics syntax

        int a = 20; // variables
        int b = 10;

        if(a>b) {  //condition 1
            System.out.print("A is Larger");
        }
        if(a==b) { //condition 2
            System.out.println("A and B equal");
        }
        else if(a!=b) { //condition 3
            System.out.println("A and B not equal");
        }
        else { //condition 4
            System.out.println("B is Larger");
        } //if-else ends


    }

}