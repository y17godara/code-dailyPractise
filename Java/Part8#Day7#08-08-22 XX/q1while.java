//Q- print "hello" x10 time with WHILE LOOP

public class q1while {
    public static void main(String args[]) {

       //while Loop
       int count = 0;

       while(count<10){
        System.out.println("Hello");
        count++; //count value increases by +1.
       }
       System.out.println("Printed Hello 10x times");

    }
    
}
